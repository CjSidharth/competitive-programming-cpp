#include<iostream>
using namespace std;
int main()
{
    // + , % , /, - , * are the operators.
    int a = 60 + 40;
    cout << a <<"\n";
    int y = 60 - 50;
    cout << y << "\n";
    int m = a*y;
    cout << m << "\n";
    int z = a/y;
    cout << z << "\n";
    int d = a%y;
    cout << d << "\n";
    return 0;
}
