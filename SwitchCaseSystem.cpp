#include<bits/stdc++.h>
using namespace std;
int main()
{
    int x = 2;
    int Temp;
    switch(x)
    {
    case 1:
        Temp = 1;
        break;
    case 2:
        Temp = 2;
        break;
    default:
        Temp = 3;
        break;
    }
    cout << "Value of temp is: " << Temp << "\n";
    return 0;
}
